public class ForSchleife
{
   public static void main(String[] args)
   {
       // Der Kopf der ForSchleife steuert die
       // Anzahl der Wiederholungen.

       // Er hat drei Abschnitte:

       // 1.) Deklaration der Zaehlveriablen i und
       // Initialisierung mit dem STARTWERT 1.
       // 2.) Redingung fuer den ENDWERT: i darf nur
       // kleiner oder gleich 20 sein (nicht grosser)
       // 3.) Die SCHRITTWEITE fuer das Hochzaehlen der
       // Variable i 
      for(int i = 1; i <= 20; i++)
      {
         
         System.out.println("Hallo " + i);
      }
   }
}
